package main.java.da_utils.combo_variables;

public class IntIntString {
	
	public int i1;
	public int i2;
	public String str;
	
	public IntIntString(int i1, int i2, String str){
		this.i1 = i1;
		this.i2 = i2;
		this.str = str;
	}
	
	public String toString(){
		return "IntIntDouble: i1=" + i1 + " i2=" + i2 + " str=" + str;
	}

}
