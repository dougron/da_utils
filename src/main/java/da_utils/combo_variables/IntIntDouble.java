package main.java.da_utils.combo_variables;

public class IntIntDouble {
	
	public int i1;
	public int i2;
	public double d;
	
	public IntIntDouble(int i1, int i2, double d){
		this.i1 = i1;
		this.i2 = i2;
		this.d = d;
	}
	
	public String toString(){
		return "IntIntDouble: i1=" + i1 + " i2=" + i2 + " d=" + d;
	}

}
